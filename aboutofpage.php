<h1> Installation </h1><br>
<li> Backup your database before installing the Re-Vu plugin for safety precautions.</li>
<li> Sign up for Re-Vu or sign in with your Facebook or Twitter account.</li>
<li><p>Copy the embed code into the wp-content/plugins directory (Be sure to maintain the directory structure of the archives, ensuring that all extracted files exist in the wp-content/plugins/re-vu-comments-system/).You can also copy paste the embed code into any HTML WordPress page.</p></li>
<li> Go to “Comments” in your blog administration to change the settings and start using Re-Vu.</li><br>

<h1>Moderation</h1>
<p>You can moderate comments from WordPress> Comments > Re-Vu.</p>

<h1>Import Comments</h1>
<p>To import existing comments, go to the admin panel WordPress> Tools > Export choose 
“All content” and download the file. Click on WordPress in re-vu.com/ru/settings/import 
and download the file.</p>

